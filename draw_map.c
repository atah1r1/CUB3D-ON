int    wall_check(float x, float y)
{
    int map_x;
    int map_y;
    if (x < 0 || x > (112 * TILE_SIZE) || y < 0 || y > g_data->map_height * TILE_SIZE)
        return 1;
    map_x = floorf(x / TILE_SIZE);
    map_y = floorf(y / TILE_SIZE);
    return (0);
}

void    draw_squar(int x, int y, int color)
{
    int i;
    int j;

    j = y;
    while (j < y + TILE_SIZE)
    {
        i = x;
        while (i < x + TILE_SIZE)
        {
            g_data->matrix[((int)j * (112 * TILE_SIZE) + (int)i)] = color;
            i++;
        }
        j++;
    }
    
}

